//função para achar o começo do labirinto
int localiza_X(char labirinto[x][30][30], int linhas) {
	int count;
	int linha_X;
	int coluna_X;
	
	for(count=0;count<linhas;count++){
		if (labirinto[x][linhas - 1][count] == 'X'){
			linha_X = linhas - 1;
			coluna_X = count;
		}
	}
	return(coluna_X);
}
//----------------------------------------------------------------------------------------------------------------------------

//função para achar o final do labirinto
int localiza_final(char labirinto[x][30][30], int linhas) {
	int count;
	int coluna_final;
	
	for(count=0;count<linhas;count++){
		if (labirinto[x][0][count] == ' '){
			coluna_final = count;
		}
	}
	return(coluna_final);
}

