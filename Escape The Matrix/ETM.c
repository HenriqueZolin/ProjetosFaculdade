char calcula_nota(int pont){
	char nota;
	if(pont <= 200){
		nota = 'D';
	} else if ((pont > 200) && (pont <= 400)){
		nota = 'C';
	} else if ((pont > 400) && (pont <= 800)){
		nota = 'B';
	} else if ((pont > 800) && (pont <= 1000)){
		nota = 'A';
	} else {
		nota = 'S';
	}
	return nota;	

}
