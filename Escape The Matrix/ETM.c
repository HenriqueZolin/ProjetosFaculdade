int menu(int lin1, int col1, int qtd, char lista[4][30]){
	int opc=0, lin2, col2, linha, i, tamMaxItem, tec=0;

 	//Achando o tamanho máximo dos itens do menu
 	tamMaxItem = strlen(lista[0]);
 	for(i=1;i<qtd;i++){
 		if(strlen(lista[i])>tamMaxItem){
 			tamMaxItem = strlen(lista[i]);	
		 }
	}
	
	lin2 = lin1 + (qtd * 2 + 2); //Final das linhas
	col2 = col1 + tamMaxItem + 4;//Final das colunas
	
	//Montando TELA DO MENU
	textColor(BLACK, _GREEN);
	setlocale(LC_ALL, "C");
	box(lin1, col1, lin2, col2);
	setlocale(LC_ALL, "");
	//Interação de opções 
	while(true){
		linha = lin1 + 2;
		for(i=0;i<qtd;i++){
			if(i+1==opc)textColor(GREEN, _BLACK);
			else textColor(BLACK, _GREEN);
			linhaCol(linha, col1+2);
			printf("%s", lista[i]); 
			linha +=2;
		}
		// Aguarda tecla
		linhaCol(1,1);
		tec = getch();
		if(tec==27){ 		//ESC
			opc=0;
			break;
		}
		else if(tec==13){ 	//ENTER
				break;
		}
		else if(tec==72){	//Seta para cima
				if(opc>1){
					opc--;
				}
		}
		else if(tec==80){ 	//Seta para baixo
				if(opc<qtd){
					opc++;
				}
			}
	}
	return opc;	
}
